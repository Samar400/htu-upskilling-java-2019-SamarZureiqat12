package WierTransfer;

public class City {
	String name;
	int id;
	Contry contry;
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public Contry getContry() {
		return contry;
	}
	public void setContry(Contry contry) {
		this.contry = contry;
	}

}
